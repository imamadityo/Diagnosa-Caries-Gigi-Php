                <div class="row">
                    <div class="col-lg-6">
                        <p align="center">Karies adalah salah satu penyakit gigi dan mulut yang banyak ditemukan di masyarakat,
                            dimana yang terkena penyakit tersebut tidak hanya orang dewasa tetapi dapat pula terjadi pada anak.
                            Karies gigi merupakan suatu penyakit pada jaringan keras gigi, yaitu email, dentin,
                            dan sementum yang disebabkan aktifitas jasad renik yang ada dalam suatu karbohidrat yang diragikan.
                            Permulaan terjadinya karies ditandai dengan larutnya permukaan email karena asam hasil metabolisme karbohidrat yang terolah oleh kuman.
                            Namun karena adanya saliva, plak, dan karang gigi, asam yang terjadi akan dinetralkan kembali (Achmad,2015).
                            Menurut WHO, karies adalah suatu proses patologis yang dimulai pada bagian luar gigi, terbatas pada suatu tempat,
                            terjadi setelah erupsi gigi dan menyebabkan penghancuran dari gigi sehingga terbentuk lubang.
                        </p>
                        <p align="center">Karies gigi merupakan penyakit yang paling banyak dijumpai di rongga mulut bersama-sama dengan penyakit periodontal,
                            sehingga merupakan masalah utama kesehatan gigi dan mulut.
                            Penyakit ini terjadi karena demineralisasi jaringan permukaan gigi oleh asam organis yang berasal dari makanan yang mengandung gula.
                            Karies gigi bersifat kronis dan dalam perkembangannya membutuhkan waktu yang lama, sehingga sebagian besar penderita mempunyai potensi mengalami gangguan seumur hidup (Solikin,2018).
                        </p>
                        <p align="center">Sebagaimana diketahui bahwa salah satu komponen dalam pembentukan karies adalah plak.
                            Insiden karies dapat dikurangi dengan melakukan penyingkiran plak secara mekanis dari permukaan gigi,
                            Peningkatan oral hygiene dapat dilakukan dengan menggunakan alat pembersih gigi yang dikombinasi dengan pemeriksaan gigi secara teratur.
                            Pemeriksaan gigi rutin ini dapat membantu mendeteksi dan memonitor masalah gigi yang berpotensi menjadi karies. Sejak erupsi/tumbuh di dalam mulut,
                            gigi sudah mempunyai risiko untuk terjadinya karies. Berat ringannya karies yang dapat terjadinya pada seseorang tergantung faktor-faktor yang ada pada manusia dan lingkungannya.
                        </p>
                        <p align="center">Menurut teori epidemiologi bahwa sebab terjadinya penyakit dan faktor- faktor yang mempengaruhi terdapatnya dalam suatu mata rantai â€host-agent- environmentâ€ (Luciawati,2017).
                            Berdasarkan teori tersebut beberapa peneliti mengembangkan konsep terjadinya karies gigi disebabkan karena adanya
                            peranan berbagai faktor yang saling berkaitan yang disebut dengan multifaktorial. Faktor- faktor tersebut adalah faktor tuan rumah (ludah dan gigi),
                            faktor agent (mikroorganisme), (substrat atau diet mengandung gula) dan faktor waktu (Achamd, 2015).
                        </p>
                        <p align="center">Karies merupakan suatu penyakit yang multifaktorial yang disebabkan oleh berbagai faktor yang berhubungan satu sama lain.
                            Banyak faktor yang dapat menimbulkan karies gigi pada anak, diantaranya adalah faktor yang ada di dalam mulut yang berhubungan langsung dengan proses terjadinya karies gigi,
                            antara lain struktur gigi (susunan gigi-geligi di rahang), derajat keasaman saliva, kebersihan mulut yang berhubungan dengan frekuensi dan kebiasaan menggosok gigi,
                            jumlah dan frekuensi makan makanan yang menyebabkan karies (kariogenik). Selain itu,
                            terdapat faktor luar sebagai faktor predisposisi dan penghambat yang berhubungan tidak langsung dengan terjadinya karies gigi antara lain usia,
                            jenis kelamin, tingkat ekonomi (Solikin,2018).
                        </p>
